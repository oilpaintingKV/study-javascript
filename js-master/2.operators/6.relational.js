// 대소 관계 비교 연산자 (Relational Operators)
// > 크다
// < 작다
// >= 크거나같다
// <= 작거나 같다
console.log(2 > 3); // false
console.log(2 < 3); // true
console.log(3 < 2); // true
console.log(3 > 2); // true
console.log(3 <= 2); // false
console.log(3 <= 3); // true
console.log(3 >= 3); // true
console.log(3 >= 2); // true


